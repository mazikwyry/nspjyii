	<h1>Skrót Historii</h1>
	<ul>
		<li>Wyry należały do parafii św. Wojciecha w Mikołowie.&nbsp;</li>
		<li> W 1919 utworzono w Wyrach kurację. Parafia erygowana została 1 VIII 1925. W 1922 rozpoczęto budowę nowego murowanego kościoła według projektu architekta Sliwki z Zabrza.&nbsp;</li>
		<li> Wykonawcą był Jan Wilk z Bytomia. Budowę ukończono w 1927.&nbsp;</li>
		<li> Wnętrze projektował artysta rzeźbiarz Konarzewski z Istebnej.&nbsp;</li>
		<li> Kościół poświęcił 27 VI 1927 dziekan dekanatu mikołowskiego ks. prałat Jan Kapica z Tychów.</li>
	</ul>
