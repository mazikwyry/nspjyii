	<p>W skład gminy Wyry wchodzą dwie miejscowości Wyry i Gostyń. Jest to jedna z najstarszych gmin na Górnym Śląsku położona na południe od Katowic na obrzeżach pięknych lasów pszczyńskich. <br><br>Pierwsza wzmianka o miejscowości Wyry pochodzi z 1287 r. Od 1770 r wieś związana była z górnictwem a w 1927 r otwarto tu zakład chemiczny. <br><br>Wieś Gostyń założona została prawdopodobnie w XIII XIV w (brak wzmianki) W 1721 r powstała tu huta szkła, wcześniej huta żelaza. Na terenie obu miejscowości w czasie kampanii wrześniowej 1939 r toczyły się jedne z najcięższych na terenie Śląska walk z wojskami hitlerowskimi. Obecnie dwa największe zakłady na terenie gminy to: 
