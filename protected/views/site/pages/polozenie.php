		<br>Na terenie gminy istnieją dwa przedszkola, dwie szkoły podstawowe, dwa gimnazja dwa ośrodki zdrowia, Dom Kultury. Działa tutaj mający kilkudziesięcioletnią tradycję chór mieszany "Zorza" oraz założony w 1930 r klub sportowy "FORTUNA" Wyry, posiadający sekcję piłki nożnej oraz skata.
