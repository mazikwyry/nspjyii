<h2 style="text-align: center;"><strong>Rok 2015</strong></h2>
Nowy 2015 Rok przywitaliśmy wraz z Chrystusem Eucharystycznym. Podczas adoracji w sylwestrową noc polecaliśmy Bogu sprawy całej parafii, prosiliśmy również w modlitwie o łaskę pokoju dla świata. <br>
<br>
25.  stycznia odbył się koncert kolęd i pastorałek. Wystąpiły: Gminny Chór „Zorza” , który obchodził swoje 95 –lecie istnienia oraz parafialne zespoły: „Dzwoneczki” i Zespół wokalno – instrumentalny.<br>
<br>
W dniach od 22. do 25. lutego, przeżywaliśmy rekolekcje wielkopostne. Wygłosił je ks. Rafał Szprync, student Katolickiego Uniwersytetu Lubelskiego.<br>
<br>
W lutym i marca kontynuowany był remont witraży w naszym Kościele.<br>
<br>
8. marca wsparliśmy naszymi ofiarami budowę kościoła na Mąkołowcu.<br>
<br>
29. kwietnia zorganizowana była w naszej parafii pielgrzymka do Sanktuarium Bożego Grobu w Miechowie i Sanktuarium Jezusa Cierpiącego w Imbramowicach.<br>
<br>
W niedzielę 10. maja miała miejsce w naszej parafii Uroczystość Pierwszej i Wczesnej Komunii Świętej. Po raz pierwszy do stołu eucharystycznego przystąpiło 37 dzieci. Do Wczesnej Komunii przystąpiło 2 dzieci.<br>
<br>
Również 10. maja zebraliśmy ofiary, na pomoc poszkodowanym w trzęsieniu ziemi w Nepalu.<br>
<br>
W sobotę 16. maja, odbyła się pielgrzymka dzieci komunijnych i ich rodziców do Pierśćca i Szczyrku.<br>
<br>
W sobotę 23. maja ks. diakon Przemysław Budniak w Katedrze Chrystusa Króla w Katowicach, przyjął święcenia prezbiteratu.<br>
<br>
W niedzielę 24. maja, ks. neoprezbiter Przemysław Budniak, odprawił swoją mszę świętą prymicyjną.<br>
8. czerwca 12 chłopców i 12 dziewczyn z  naszej parafii w kościele Męczeństwa Świętego Jana Chrzciciela w Łaziskach Średnich przyjęło z rąk ks. abp. Wiktora Skworca sakrament bierzmowania.<br>
<br>
W niedzielę 14. czerwca przeżywaliśmy odpust parafialny. Uroczystej sumie odpustowej przewodniczył i kazanie wygłosił ks. Rafał Szprync. Przed kościołem zebrane zostały ofiary na organizację Światowych Dni Młodzieży.<br>
<br>
W czerwcu zakończyły się prace związane z naprawą i wymianą witraży w naszym kościele. Serdeczne Bóg zapłać za wszystkie składane ofiary na ten cel.<br>
<br>
Dekretem ks. arcybiskupa Wiktora Skworca, z dniem 25 lipca ks. proboszcz Aleksander Wojtala zakończył posługę w naszej parafii i został przeniesiony do Katowic Szopienic. Od 26. lipca posługę w naszej parafii rozpoczął ks. Henryk Skorupa, dotychczasowy wikariusz w Łaziskach Średnich.<br>
<br>
W dniach od 19. do 22. sierpnia, z naszej parafii wyruszyła XXIII piesza pielgrzymka na Jasną Górę. Pielgrzymkę  poprowadzili ks. Piotr Stoll. W pieszej pielgrzymce uczestniczyło 160 pątników. W ostatnim dniu do pielgrzymów pieszych dołączyło 140 pielgrzymów, którzy przybyli do Częstochowy autokarami oraz pielgrzymi samochodowi. Przed Cudownym Obrazem Królowej Polski pielgrzymi uczestniczyli we Mszy Świętej. Przed mszą została odprawiona Droga Krzyżowa, a w godzinach popołudniowych pielgrzymi zgromadzili się na wspólnej modlitwie – Koronce do Miłosierdzia Bożego. Wieczorem wszyscy spotkali się w naszej parafialnej świątyni, by podziękować Bożemu Sercu za szczęśliwy powrót do domu. Już dziś zapraszamy na XXIV pieszą pielgrzymkę do Częstochowy i dziękujemy wszystkim sponsorom i zaangażowanym w organizowanie naszej pielgrzymki.<br>
<br>
Dekretem ks. arcybiskupa Wiktora Skworca, z dniem 29. sierpnia ks. wikary Zbigniew Glenc został przeniesiony do parafii w Mysłowicach Janowie Miejskim.<br>
<br>
1. września rezydentem w naszej parafii został ks. Adam Kiedrzyn, dyrektor Księgarni Świętego Jacka.<br>
<br>
20. września przed kościołem zebrane zostały ofiary na organizację Światowych Dni Młodzieży.<br>
<br>
13. października w czasie nabożeństwa fatimskiego gościliśmy Elżbietę Ogermann z Zespołu Śląsk i Krzysztofa Jasińskiego, którzy uświetnili naszą wspólną modlitwę swoim śpiewem.<br>
<br>
W październiku został postawiony nowy krzyż na cmentarzu parafialnym. Dziękujemy fundatorowi i zarazem wykonawcy.<br>
<br>
W listopadzie miały miejsce prace związane z upiększeniem terenu przed i wokół kościoła. Dziękujemy wszystkim zaangażowanym w to dzieło.<br>
<br>
Na początku grudnia rozpoczęliśmy tynkowanie naszego kościoła.<br>
<br>
18. grudnia  dzieci z Ośrodka Szkolno - Rehabilitacyjnego w Wyrach w raz z wychowawcami  i opiekunami wystawiły jasełka oraz uczestniczyli we Mszy Świętej.<br>
<br>
Jak co roku pół godziny przed Pasterką młodzież oazowa przygotowała czuwanie. Wprowadzenie do przeżycia Mszy Narodzenia Pańskiego miało charakter przedstawienia.<br>
<br>
Mijający rok podsumowaliśmy w czasie Mszy Świętej i nabożeństwa na zakończenie roku kalendarzowego, w sylwestrowy wieczór o godz. 17.00. Za wszystkie łaski, które otrzymaliśmy od Boga w mijającym roku dziękowaliśmy podczas adoracji w sylwestrową noc. Rok 2015 zakończyliśmy odśpiewaniem o północy uroczystego Te Deum laudamus.
