  <h1>Formacja studentów</h1>

  Formacja studentów to najmłodsza wspólnota w naszej parafii, która powstała w 2009 roku z inicjatywy studentów naszej parafii. Spotykamy się i rozmawiamy o tym co nas interesuje o bierzących sprawach i problemach w Kościele. Studenci udzielają się w parafii m.in. tworząc trzeci ołtarz na Boże Ciało czy biorąc udział w przedstawieniach ewangelizacyjnych. 
  <br/></br>
  Do grupy nie należą tylko studenci, więc jeśli szukasz wspólnoty dla siebie to zapraszamy na spotkania w każdy <strong>pierwszy piątek misiąca po mszy młodzieżowej</strong> (za wyjątkiem wakacji) w salkach.

